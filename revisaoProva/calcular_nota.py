def gerar_boletins(dados):
    agrupado = {}

    for registro in dados:
        aluno = registro["aluno"]
        materia = registro["materia"]
        nota = registro["nota"]

        if aluno not in agrupado:
            agrupado[aluno] = {}

        if materia not in agrupado[aluno]:
            agrupado[aluno][materia] = []

        agrupado[aluno][materia].append(nota)

    boletins = {}

    for aluno, materias in agrupado.items():
        medias_disciplinas = {}

        for materia, notas in materias.items():
            notas_calc = notas[:]

            while len(notas_calc) > 2:
                pior = min(notas_calc)
                notas_calc.remove(pior)

            media_materia = round(sum(notas_calc) / len(notas_calc), 2)
            medias_disciplinas[materia] = media_materia

        media_geral = round(sum(medias_disciplinas.values()) / len(medias_disciplinas), 2)

        if media_geral >= 7.0:
            status = "Aprovado"
        elif media_geral >= 5.0:
            status = "Recuperacao"
        else:
            status = "Reprovado"

        boletins[aluno] = {
            "medias_disciplinas": medias_disciplinas,
            "media_geral": media_geral,
            "status": status
        }

    lista_ranking = [(aluno, boletins[aluno]["media_geral"]) for aluno in boletins]

    lista_ranking_ordenada = sorted(lista_ranking, key=lambda x: (-x[1], x[0]))

    ranking = [nome for nome, media in lista_ranking_ordenada]

    return {
        "boletins": boletins,
        "ranking": ranking
    }


